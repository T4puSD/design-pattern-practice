package org.tapusd.chapterfour.pizzas;

import org.tapusd.chapterfour.factory.ingredients.cheese.Cheese;
import org.tapusd.chapterfour.factory.ingredients.clams.Clams;
import org.tapusd.chapterfour.factory.ingredients.dough.Dough;
import org.tapusd.chapterfour.factory.ingredients.peperroni.Pepperoni;
import org.tapusd.chapterfour.factory.ingredients.sauce.Sauce;
import org.tapusd.chapterfour.factory.ingredients.veggies.Veggies;

import java.util.Arrays;


public abstract class Pizza {

    String name;
    Dough dough;
    Sauce sauce;
    Veggies [] veggies;
    Cheese cheese;
    Pepperoni pepperoni;
    Clams clams;


    public abstract void prepare();

    public void bake() {
        System.out.println("Bake for 25 minutes at 350");
    }

    public void cut() {
        System.out.println("Cutting the pizza into diagonal slices");
    }

    public void box() {
        System.out.println("Place pizza in official PizzaStore box");
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Dough getDough() {
        return dough;
    }

    public void setDough(Dough dough) {
        this.dough = dough;
    }

    public Sauce getSauce() {
        return sauce;
    }

    public void setSauce(Sauce sauce) {
        this.sauce = sauce;
    }

    public Veggies[] getVeggies() {
        return veggies;
    }

    public void setVeggies(Veggies[] veggies) {
        this.veggies = veggies;
    }

    public Cheese getCheese() {
        return cheese;
    }

    public void setCheese(Cheese cheese) {
        this.cheese = cheese;
    }

    public Pepperoni getPepperoni() {
        return pepperoni;
    }

    public void setPepperoni(Pepperoni pepperoni) {
        this.pepperoni = pepperoni;
    }

    public Clams getClams() {
        return clams;
    }

    public void setClams(Clams clams) {
        this.clams = clams;
    }

    @Override
    public String toString() {
        return "Pizza{" +
                "name='" + name + '\'' +
                ", dough='" + dough + '\'' +
                ", sauce='" + sauce + '\'' +
                ", veggies=" + Arrays.toString(veggies) +
                ", cheese=" + cheese +
                ", pepperoni=" + pepperoni +
                ", clams=" + clams +
                '}';
    }
}
