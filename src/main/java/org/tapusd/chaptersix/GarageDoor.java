package org.tapusd.chaptersix;

public class GarageDoor {
    public void up() {
        System.out.println("Garage Door OPENED");
    }
    public void down() {
        System.out.println("Garage Door CLOSED");
    }
    public void stop() {
        System.out.println("Garage Door STOPPED");
    }
    public void lightOn() {
        System.out.println("Garage light Turned ON");
    }
    public void lightOff() {
        System.out.println("Garage light Turned OFF");
    }
}
